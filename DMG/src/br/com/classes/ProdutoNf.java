package br.com.classes;

public class ProdutoNf {
	private int produtoNfId = 0;
	private int codProd = 0;
	private int qtd = 0;
	private double vlrUnit = 0;
	private double total = 0;
	private int notaFiscal = 0;
	public int getProdutoNfId() {
		return produtoNfId;
	}
	public void setProdutoNfId(int produtoNfId) {
		this.produtoNfId = produtoNfId;
	}
	public int getCodProd() {
		return codProd;
	}
	public void setCodProd(int codProd) {
		this.codProd = codProd;
	}
	public int getQtd() {
		return qtd;
	}
	public void setQtd(int qtd) {
		this.qtd = qtd;
	}
	public double getVlrUnit() {
		return vlrUnit;
	}
	public void setVlrUnit(double vlrUnit) {
		this.vlrUnit = vlrUnit;
	}
	public double getTotal() {
		return total;
	}
	public void setTotal(double total) {
		this.total = total;
	}
	public int getNotaFiscal() {
		return notaFiscal;
	}
	public void setNotaFiscal(int notaFiscal) {
		this.notaFiscal = notaFiscal;
	}
	
	
}
